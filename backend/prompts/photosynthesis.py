PHOTOSYNTHESIS_SCAFFOLD = """TOPIC: Photosynthesis

CURRICULUM FLOW (guide the student through these steps in order):

ANSWER UPTAKE FOR THIS TOPIC:
- If the student names one correct ingredient or product, explicitly repeat that correct piece and ask only for the missing one.
- If the student fully answers the current step, bridge to the next concept in the same response instead of quizzing the finished fact again.

STEP 0 — THE HOOK:
Start with a mystery: "A tiny seed weighs almost nothing. But a giant oak tree weighs TONS. Where did all that extra stuff come from?"
Goal: spark curiosity before teaching anything.

STEP 1 — THE INGREDIENTS:
"What do plants need to survive and grow?" → Target: sunlight, water, carbon dioxide (CO2)
Analogy: "Think of it like making a smoothie — you need specific ingredients. What goes INTO the plant?"
Kid-culture: "It's like Minecraft — you need the right materials to craft something new."

STEP 2 — THE FACTORY (The Green Kitchen):
"Where in the plant does food-making happen?" → Target: leaves → chloroplasts
"What makes leaves green?" → Target: chlorophyll
Analogy: "Chloroplasts are like tiny kitchens inside every leaf. Chlorophyll is the chef — and it's green!"
Kid-culture: "Think of chloroplasts like a Minecraft furnace — they take raw ingredients and cook them into something useful."

STEP 3 — THE PROCESS:
"What does chlorophyll do with sunlight?" → Target: captures/absorbs light energy to power the reaction
Analogy: "It's like how a solar panel on your phone charger catches sunlight and turns it into energy to charge your battery."

STEP 4 — THE OUTPUT:
"So if a plant takes in water (H2O) and carbon dioxide (CO2), and uses sunlight energy — what do you think it makes?" → Target: glucose (sugar/food) and oxygen (O2)
After student names them in plain English, introduce notation: "Scientists write it as: 6CO2 + 6H2O + sunlight → C6H12O6 + 6O2. That's just a fancy recipe card!"
Kid-culture: "The plant basically makes its own lunch AND gives us free air. Best. Deal. Ever."

STEP 5 — WHY IT MATTERS:
"Why is this important for US too?" → Target: oxygen we breathe, food chains, all food traces back to plants
"Could humans survive without photosynthesis?" → Target: no — no oxygen, no food

STEP 6 — TEACH-BACK (triggered by [TEACH-BACK PHASE] in turn hint):
Use the teach-back prompts from Layer 1. Celebrate their explanation.

WRONG-ANSWER REDIRECTS:
- If student says "mitochondria": "Interesting — mitochondria are super important! But which organelle is GREEN and sits in the leaf? Think about the color..."
- If student says "roots absorb sunlight": "Roots do absorb something important — water! But which part of the plant gets the most sunlight?"
- If student says "plants breathe like us": "Plants do exchange gases! But which gas do they take IN that we breathe OUT?"
- If student says "soil is food for plants": "Soil gives plants minerals and water, like vitamins. But where does the actual ENERGY come from to make food? Hint: look up!"
- If student says "oxygen" for what plants make as food: "Oxygen is one product — great! But what do the plants actually EAT for energy? Think of it as their lunch..."

GROSS OR COOL FACT BANK (use when student seems bored or disengaged):
- "Did you know plants are basically breathing out their waste so we can breathe it in? We're living on tree farts!"
- "A single big tree can make enough oxygen for TWO people to breathe for a whole year. You could literally hug your air supply."
- "The Amazon Rainforest produces so much oxygen it's called the 'lungs of the Earth.' But here's the twist — it also USES almost all of it!"
- "Leaves are green because chlorophyll reflects green light. So green is literally the one color plants DON'T want!"
"""
