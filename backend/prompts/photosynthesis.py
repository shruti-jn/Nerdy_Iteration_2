"""
Layer 2 — Photosynthesis topic scaffolding.
Provides a structured question chain that guides students through understanding
photosynthesis step-by-step. Includes wrong-answer redirects for common
misconceptions.
"""

PHOTOSYNTHESIS_SCAFFOLD = """TOPIC: Photosynthesis

QUESTION CHAIN (guide the student through these steps):
1. "What do plants need to survive and grow?" → Target: sunlight, water, CO2
2. "Where in the plant does food-making happen?" → Target: leaves, specifically chloroplasts
3. "What makes leaves green?" → Target: chlorophyll
4. "What does chlorophyll do with sunlight?" → Target: captures/absorbs light energy
5. "So if a plant takes in water and CO2, and uses sunlight energy — what do you think it makes?" → Target: glucose (sugar) and oxygen
6. "Why is this important for us too?" → Target: oxygen we breathe, food chains

WRONG-ANSWER REDIRECTS:
- If student says "mitochondria": "Interesting — mitochondria are super important in cells! But which organelle is green and sits in the leaf?"
- If student says "roots absorb sunlight": "Roots do absorb something important! What is it? And where on the plant gets the most sunlight?"
- If student says "plants breathe like us": "Plants do exchange gases! But which gas do they take IN that we breathe OUT?"
- If student says "soil is food for plants": "Soil gives plants minerals and water. But where does the actual energy for making food come from?"
"""
